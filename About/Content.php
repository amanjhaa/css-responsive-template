                <h3>About</h3>
                <p>We welcome learners of all ages to the Online Lib, from school students and teachers to families and community groups. Our collection – filled with treasures including ancient manuscripts, world maps, historic newspapers, music recordings, and contemporary photographs – is at the heart of our unique learning programme which offers a range of opportunities to explore, connect and interact both onsite, online and nationally.
                </p>
                <p>Each year, over 5,000 participants take part in a rich mix of creative programmes including workshops, masterclasses, performances, courses and discovery days. Led by our team of specialist educators, the programme combines opportunities to encounter original and rare objects together with a skills-based approach centered around critical thinking, research, and visual, verbal and digital literacy.</p>

                <p>Learners come to our website every year to explore a wealth of subjects from Gothic literature and medieval history to First World War propaganda and feminism. Users can access thousands of digitised treasures from our collection as well as fascinating interpretative essays, films, animations and teachers’ notes.</p>

                <p>Together, the Libraries hold more than 1 million printed items, over 80,000 e-journals and outstanding special collections including rare books and manuscripts, classical papyri, maps, music, art and printed ephemera.</p>

